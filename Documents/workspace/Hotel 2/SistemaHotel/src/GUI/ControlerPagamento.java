    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        pagamentoVO.setCodPagamento(Integer.parseInt(txtCodigo.getText()));
        pagamentoVO.setQtdParcela(Integer.parseInt(txtParcela.getText()));
        pagamentoVO.setValorParcelado(Double.parseDouble(txtValorParcelado.getText()));
        pagamentoVO.setValorTotal(Double.parseDouble(txtValorTotal.getText()));
        
        
        //Aqui insere no banco de dados
        if (pagamentoDao.cadastrar(pagamentoVO) == true)
            JOptionPane.showMessageDialog (rootPane, "PAGAMENTO REGISTRADO!!!");
        else
            JOptionPane.showMessageDialog (rootPane, "ERRO: PAGAMENTO NÃO REGISTRADO!!");                   
    }//GEN-LAST:event_btnCadastrarActionPerformed

