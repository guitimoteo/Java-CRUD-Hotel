    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        consumoVO = new Consumo();
        consumoVO.setCodConsumo(Integer.parseInt(txtCodigo.getText()));
        consumoVO.setValor(Double.parseDouble(txtValor.getText()));
        
        
       /**
        * Nem todo produto foi consumido
        * Nem todo servico foi utilizado
        * Não existe produto Consumido
        * Não existe servico utilizado
        */
        
        if (txtProduto.getText().length() == 0 ) 
            consumoVO.setProduto(null);
        
        else{ //Procura algum produto
            Object obj = produtoDao.buscar(Integer.parseInt(txtProduto.getText()));
            
        
        if (obj == null){
            JOptionPane.showMessageDialog(rootPane, "Produto Inválido!!");
            return;
        }
        else { 
            consumoVO.setProduto((Produto)obj);
        
        
        if(txtServico.getText().length() == 0)
            consumoVO.setServico(null);
        
        else {//Procura algum serviço
            
            /**
             * Mudei o nome da variavel para "object" em servico 
             * devido a "obj" já existir para o produto,
             * por isso dá erro quando crio o mesmo nome em servico.
             */
            Object object = servicoDao.buscar(Integer.parseInt(txtServico.getText()));
            
         if(object == null){
             JOptionPane.showMessageDialog (rootPane, "Serviço Inválido!!");
             return;
         }
         else{
             consumoVO.setServico((Servico)object);
         }        
         }         
}
        
        //Aqui insere no banco de dados
        if (consumoDao.cadastrar(consumoVO) == true)
            JOptionPane.showMessageDialog (rootPane, "CONSUMO REGISTRADO!!");
        else
            JOptionPane.showMessageDialog (rootPane, "ERRO: CONSUMO NÃO REGISTRADO!!");
    }//GEN-LAST:event_btnCadastrarActionPerformed
        }

