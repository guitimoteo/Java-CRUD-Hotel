    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       ContaDAO contaDAO= new ContaDAO();
        Conta conta1 = new Conta();

        if (txtCodigo.getText().isEmpty() || txtProduto.getText().isEmpty()|| txtServico.getText().isEmpty()
                || txtQuantidadeProduto.getText().isEmpty() || txtQuantidadeServico.getText().isEmpty() 
                || txtDataEntrada.getText().isEmpty() || txtDataSaida.getText().isEmpty()
                || txtValorHospedagem.getText().isEmpty() || txtValorConsumo.getText().isEmpty() 
                || txtValorTotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Erro!! Por favor preencha os campos corretamente!!!");
        } else {

            conta1.setCodConta(Integer.parseInt(txtCodigo.getText()));
            conta1.setProdConsumo(txtProduto.getText());
            conta1.setServConsumo(txtServico.getText());
            conta1.setQtdProduto(Integer.parseInt(txtQuantidadeProduto.getText()));
            conta1.setQtdServico(Integer.parseInt(txtQuantidadeServico.getText()));
            conta1.setDataEntrada(txtDataEntrada.getText());
            conta1.setDataSaida(txtDataSaida.getText());
            conta1.setValorHospedagem(Double.parseDouble(txtValorHospedagem.getText()));
            conta1.setValorConsumo(Double.parseDouble(txtValorConsumo.getText()));
            conta1.setValorTotal(Double.parseDouble(txtValorTotal.getText()));
            
            contaDAO.cadastrar(conta1);
            JOptionPane.showMessageDialog(this, "Conta registrada com sucesso!!!");
            //Limpa toda os campos da tela
            txtCodigo.setText("");
            txtProduto.setText("");
            txtServico.setText("");
            txtQuantidadeProduto.setText("");
            txtQuantidadeServico.setText("");
            txtDataEntrada.setText("");
            txtDataSaida.setText("");
            txtValorHospedagem.setText("");
            txtValorConsumo.setText("");
            txtValorTotal.setText("");
            
            //Volta o focus do mouse para o nome.
            txtCodigo.requestFocus();

                                           
                                  
       
    }//GEN-LAST:event_btnCadastrarActionPerformed
    }
