    public void rodaRelogio() {  
        try {  
            while (true) {  
                Date data = new Date(); 
                txtHora.setText(formatter.format(data));  
                txtData.setText(formatter1.format(data));  
                
                Thread.sleep(1000);  
            }  
        } catch (InterruptedException ex) {  
        }  

    }
    

