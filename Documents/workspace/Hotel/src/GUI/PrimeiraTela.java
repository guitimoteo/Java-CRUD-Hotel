    private void btnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcessarActionPerformed
        Funcionario funcionario = new Funcionario();

        FuncionarioDAO funcionariodao = new FuncionarioDAO();

        String login = txtLogin.getText();
        String senha = txtSenha.getText();


        if (txtLogin.getText().isEmpty() || txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ERRO: Há campos em branco. Por favor verifique novamente!!!");
        } else {

            boolean resposta = funcionariodao.logar(txtLogin.getText(), txtSenha.getText());

            if (resposta == true) {
                JOptionPane.showMessageDialog(null, "Login realizado com sucesso!");
                
                 // Aqui acessa o Menu do sistema
                Menu menu = new Menu();
                menu.show();
                this.setVisible(false);

                 
             
            } else {
                JOptionPane.showMessageDialog(null, "ERRO: Login ou senha está inválido!!!");
            }
        }
      
        
               
    }//GEN-LAST:event_btnAcessarActionPerformed

