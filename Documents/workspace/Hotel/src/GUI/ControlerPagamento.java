    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       PagamentoDAO pagamentoDAO= new PagamentoDAO();
        Pagamento pagamento1 = new Pagamento();

        if (txtCodigo.getText().isEmpty() || txtFormaPagamento.getText().isEmpty()|| txtParcela.getText().isEmpty()
                || txtValorParcelado.getText().isEmpty() || txtValorTotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Erro!! Por favor preencha os campos corretamente!!!");
        } else {

            pagamento1.setCodPagamento(Integer.parseInt(txtCodigo.getText()));
            pagamento1.setFormaPagamento(txtFormaPagamento.getText());
            pagamento1.setQtdParcela(Integer.parseInt(txtParcela.getText()));
            pagamento1.setValorParcelado(Double.parseDouble(txtValorParcelado.getText()));
            pagamento1.setValorTotal(Double.parseDouble(txtValorTotal.getText()));
            
            
            pagamentoDAO.cadastrar(pagamento1);
            JOptionPane.showMessageDialog(this, "Pagamento cadastrado com sucesso!!!");
            //Limpa toda os campos da tela
            txtCodigo.setText("");
            txtFormaPagamento.setText("");
            txtParcela.setText("");
            txtValorParcelado.setText("");
            txtValorTotal.setText("");
            
            //Volta o focus do mouse para o nome.
            txtCodigo.requestFocus();

        }                                 
                                                           
    }//GEN-LAST:event_btnCadastrarActionPerformed

