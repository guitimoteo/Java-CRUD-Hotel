    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
              if (txtCodigo.getText().isEmpty()){
                  JOptionPane.showMessageDialog (rootPane, "Por favor, insira o código do pagamento para buscar os dados");
              }else{
                    pagamentoVO = new Pagamento ();
                    pagamentoVO = (Pagamento)pagamentoDao.buscar(Integer.parseInt(txtCodigo.getText()));     
            if (pagamentoVO == null)
                JOptionPane.showMessageDialog (rootPane, "Pagamento não encontrado!!!");
            else{       
                txtFormaPagamento.setText(pagamentoVO.getFormaPagamento());
                txtParcela.setText(String.format("%1", pagamentoVO.getQtdParcela()));
                txtValorParcelado.setText("R$ " + String.format("%10.2f",pagamentoVO.getValorParcelado()));
                txtValorTotal.setText("R$ " + String.format("%10.2f",pagamentoVO.getValorTotal()));
            }
              }
        
     }catch(HeadlessException | NumberFormatException e){
     }   
    }//GEN-LAST:event_btnBuscarActionPerformed

