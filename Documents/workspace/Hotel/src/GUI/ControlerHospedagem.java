    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        HospedagemDAO hospedagemDAO= new HospedagemDAO();
        Hospedagem hospedagem1 = new Hospedagem();

        if (txtCodigo.getText().isEmpty() || txtHospede.getText().isEmpty()
                || txtDataEntrada.getText().isEmpty() || txtDataSaida.getText().isEmpty() || txtHora.getText().isEmpty()
                || txtQuarto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Erro!! Por favor preencha os campos corretamente!!!");
        } else {

            hospedagem1.setCodHospedagem(Integer.parseInt(txtCodigo.getText()));
            hospedagem1.setNomeHospede(txtHospede.getText());
            hospedagem1.setDataEntrada(txtDataEntrada.getText());
            hospedagem1.setDataSaida(txtDataSaida.getText());
            hospedagem1.setHora(txtHora.getText());
            
            
            
            hospedagemDAO.cadastrar(hospedagem1);
            JOptionPane.showMessageDialog(this, "Hospedagem cadastrada com sucesso!!!");
            //Limpa toda os campos da tela
            txtCodigo.setText("");
            txtHospede.setText("");
            txtDataEntrada.setText("");
            txtDataSaida.setText("");
            txtHora.setText("");
            txtQuarto.setText("");
            
            //Volta o focus do mouse para o codigo Servico
            txtCodigo.requestFocus();

       

    }                                    
    }//GEN-LAST:event_btnCadastrarActionPerformed

