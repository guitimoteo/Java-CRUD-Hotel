    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
         try{
            if (txtCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog (rootPane, "Por favor, insira o código da Hospedagem!!!");
            }else{
                 hospedagemVO = new Hospedagem ();
                 hospedagemVO = (Hospedagem)hospedagemDao.buscar(Integer.parseInt(txtCodigo.getText()));    
            if (hospedagemVO == null)         
                 JOptionPane.showMessageDialog (rootPane, "Registro não encontrado");
            else{
                 //Pergunta ao usuário se ele deseja deletar o registro.
            if(JOptionPane.showConfirmDialog(rootPane, "Deseja deletar o registro de " + hospedagemVO.getNomeHospede() + "(nome do hóspede:) "
                  + hospedagemVO.getNomeHospede(), null, JOptionPane.YES_NO_OPTION)==0)
                {
           if(hospedagemDao.deletar(hospedagemVO)) //Verifica a existência de erros
                  JOptionPane.showMessageDialog (rootPane, "Registro deletado com êxito");
           else
                    JOptionPane.showMessageDialog (rootPane, "Não foi possível deletar o registro");
           }

  }
       
     }
     
   }catch(HeadlessException | NumberFormatException e){
          JOptionPane.showMessageDialog (rootPane, "ERRO: " + e.getMessage());

 
  
    }                      
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
              if (txtCodigo.getText().isEmpty()){
                  JOptionPane.showMessageDialog (rootPane, "Por favor, insira o código da hospedagem para buscar os dados");
              }else{
                    hospedagemVO = new Hospedagem ();
                    hospedagemVO = (Hospedagem)hospedagemDao.buscar(Integer.parseInt(txtCodigo.getText()));     
            if (hospedagemVO == null)
                JOptionPane.showMessageDialog (rootPane, "Hospedagem não encontrada!!!");
            else{       
                txtHospede.setText(hospedagemVO.getNomeHospede());
                txtDataEntrada.setText(hospedagemVO.getDataEntrada());
                txtDataSaida.setText(hospedagemVO.getDataSaida());
                txtHora.setText(hospedagemVO.getHora());      
                txtQuarto.setText(String.format("%1", hospedagemVO.getQuarto()));
   
            }
    
        }
   

     }catch(HeadlessException | NumberFormatException e){
          

  JOptionPane.showMessageDialog (rootPane, "ERRO: " + e.getMessage());
       

 }


    
                                 
    }//GEN-LAST:event_btnBuscarActionPerformed

